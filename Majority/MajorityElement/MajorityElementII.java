    public List<Integer> majorityElement(int[] nums) {

        List<Integer> result = new ArrayList<>();

        int firstLeader = 0;
        int firstCount = 0;

        int secondLeader = 0;
        int secondCount = 0;

        for (int i = 0; i < nums.length; i++) {
            int current = nums[i];

            if (firstCount == 0) {
                if (secondCount == 0) {
                    firstLeader = current;
                    firstCount++;
                } else {

                    if (current == secondLeader) {
                        secondCount++;
                    } else {
                        firstLeader = current;
                        firstCount++;
                    }
                }
            } else {

                // there is an existing first leader

                if (firstLeader == current) {
                    firstCount++;
                } else {
                    if (secondCount == 0) {
                        secondLeader = current;
                        secondCount = 1;
                    } else if (secondLeader == current) {
                        secondCount++;
                    } else {

                        firstCount--;
                        secondCount--;
                    }
                }
            }
        }

        if (firstCount > 0 && isValid(nums, firstLeader)) {
            result.add(firstLeader);
        }

        if (secondCount > 0 && isValid(nums, secondLeader)) {
            result.add(secondLeader);
        }

        return result;
    }

    private boolean isValid(int[] nums, int target) {
        int count = 0;
        for (int i = 0; i < nums.length; i++) {
            if (nums[i] == target) {
                count++;
            }
        }

        return count > (nums.length / 3);
    }

